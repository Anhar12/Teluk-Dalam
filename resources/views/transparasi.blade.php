            <div class="text-light font-medium text-md text-justify mb-10">
                Anggaran pendapatan dan belanja desa (APBDes) adalah peraturan desa yang memuat sumber-sumber penerimaan
                dan alokasi pengeluaran desa dalam kurun waktu satu tahun. APB Desa terdiri atas bagian pendapatan Desa,
                belanja Desa dan pembiayaan. Rancangan APB Desa dibahas dalam musyawarah perencanaan pembangunan desa.
                Kepala Desa bersama BPD menetapkan APB Desa setiap tahun dengan Peraturan Desa.
            </div>

            <div class="text-justify mb-10 space-y-5">
                <h4 class="text-dark font-semibold text-lg">
                    Sumber Dana APBDesa
                </h4>
                <p class="text-light font-medium text-md text-justify">
                    Menurut UU 32/2004 dan PP 72/2005 menyebutkan sumber-sumber pendapatan desa meliputi:
                </p>
                <ol class="list-decimal text-light font-medium text-md text-justify px-4 space-y-3">
                    <li>
                        Pendapatan asli desa, terdiri dari hasil usaha desa, hasil kekayaan desa, hasil swadaya dan
                        partisipasi, hasil gotong royong, dan lain-lain pendapatan asli desa yang sah,
                    </li>
                    <li>
                        bagi hasil pajak daerah Kabupaten/Kota paling sedikit 1.0% (sepuluh per seratus) untuk desa dan
                        dari retribusi Kabupaten/Kota sebagian diperuntukkan bagi desa,
                    </li>
                    <li>
                        bagian dari dana perimbangan keuangan pusat dan daerah yang diterima oleh Kabupaten/Kota untuk
                        Desa paling sedikit 10% (sepuluh per seratus), yang pembagiannya untuk setiap Desa secara
                        proporsional yang merupakan alokasi dana desa,
                    </li>
                    <li>
                        bantuan keuangan dari Pemerintah, Pemerintah Provinsi, dan Pemerintah Kabupaten/Kota dalam
                        rangka pelaksanaan urusan pemerintahan,
                    </li>
                    <li>
                        hibah dan sumbangan dari pihak ketiga yang tidak mengikat.
                    </li>
                </ol>
            </div>

            <div class="text-justify mb-10 space-y-5">
                <h4 class="text-dark font-semibold text-lg">
                    Fungsi APBDesa
                </h4>
                <p class="text-light font-medium text-md text-justify">
                    APBDesa memiliki fungsi, sebagai berikut:
                </p>
                <ol class="list-decimal text-light font-medium text-md text-justify px-4 space-y-3">
                    <li>
                        Alat perencanaan
                        <ul class="list-disc px-4 space-y-1">
                            <li>
                                Merumuskan tujuan dan sasaran kebijakan agar sejalan dengan visi, misi dan sasaran yang
                                sudah ditetapkan.
                            </li>
                            <li>
                                Merencanakan berbagai program, kegiatan, serta sumber pendapatan.
                            </li>
                            <li>
                                Mengalokasikan dana untuk program dan kegiatan yang sudah disusun.
                            </li>
                            <li>
                                Menentukan indikator kinerja dan pencapaian strategi.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Alat pengendalian
                        <p>
                            Anggaran berisi perencanaan detail atas pendapatan dan pengeluaran desa, dimaksudkan dengan
                            adanya anggaran, semua bentuk pengeluaran dan pemasukan dapat dipertanggungjawabkan kepada
                            publik. Tanpa adanya anggaran, desa akan sulit mengendalikan pengeluaran dan pemasukan.
                        </p>
                    </li>
                    <li>
                        Alat koordinasi dan komunikasi
                        <p>
                            Dalam menyusun anggaran, pasti antar unit kerja akan melakukan komunikasi dan koordinasi.
                            Dalam perencanaan dan pelaksanaan anggaran harus dikomunikasikan ke seluruh perangkat desa.
                            Anggaran publik yang disusun dengan baik akan mampu mendeteksi terjadinya ikonsistensi suatu
                            unit kerja di dalam pencapaian tujuan desa.
                        </p>
                    </li>
                    <li>
                        Alat penilaian kinerja
                        <p>
                            Perencanaan anggaran dan pelaksanaannya akan menjadi penilaian kinerja perangkat desa.
                            Kinerja perangkat desa akan dinilai berdasarkan pencapaian target anggaran serta pelaksanaan
                            efisiensi anggaran. Anggaran merupakan alat yang efektif untuk melakukan pengendalian dan
                            penilaian kinerja.
                        </p>
                    </li>
                    <li>
                        Alat motivasi
                        <p>
                            Anggaran dapat digunakan untuk memberi motivasi kepada perangkat desa dalam bekerja secara
                            efektif dan efisien. Dengan membuat anggaran yang tepat dan dapat melaksanakannya sesuai
                            target dan tujuan desa, maka desa dikatakan mempunyai kinerja yang baik.
                        </p>
                    </li>
                </ol>
            </div>

            <div class="text-justify mb-10 space-y-5">
                <h4 class="text-dark font-semibold text-lg">
                    Ketentuan Penyusunan APBDesa
                </h4>
                <ol class="list-decimal text-light font-medium text-md text-justify px-4 space-y-3">
                    <li>
                        APB Desa disusun berdasarkan RKPDesa yang telah ditetapkan dengan Perdes.
                    </li>
                    <li>
                        APB Desa disusun untuk masa 1 (satu) tahun anggaran, terhitung mulai 1 Januari sampai 31
                        Desember tahun berikutnya.
                    </li>
                    <li>
                        Prioritas Belanja Desa disepakati dalam Musyawarah Desa dan Musyawarah Perencanaan Pembangunan
                        Desa berdasarkan pada penilai kebutuhan masyarakat.
                    </li>
                    <li>
                        Rancangan APB Desa harus dibahas bersama dengan Badan Permusyawaratan Desa (BPD).
                    </li>
                    <li>
                        APB Desa dapat disusun sejak bulan September dan harus ditetapkan dengan Perdes,
                        selambat-lambatnya pada 31 Desember pada tahun yang sedang dijalani.
                    </li>
                </ol>
            </div>

